/**
 * add - function to add two ints
 * @a: first int
 * @b: second int
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - function to subtract two ints
 * @a: first int
 * @b: second int
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - function to multiply two ints
 * @a: first int
 * @b: second int
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - function to add divide ints
 * @a: first int
 * @b: second ints
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - function to find the remainder of two ints
 * @a: first int
 * @b: second int
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
